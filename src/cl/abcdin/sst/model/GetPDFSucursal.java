package cl.abcdin.sst.model;

public class GetPDFSucursal {
	String sRutt;
	String sFolio;
	String sDoc;
	String sMonto;
	String sFecha;
	String sRuttt;
	String bMerito;
	
	public String getsRutt() {
		return sRutt;
	}
	public void setsRutt(String sRutt) {
		this.sRutt = sRutt;
	}
	public String getsFolio() {
		return sFolio;
	}
	public void setsFolio(String sFolio) {
		this.sFolio = sFolio;
	}
	public String getsDoc() {
		return sDoc;
	}
	public void setsDoc(String sDoc) {
		this.sDoc = sDoc;
	}
	public String getsMonto() {
		return sMonto;
	}
	public void setsMonto(String sMonto) {
		this.sMonto = sMonto;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsRuttt() {
		return sRuttt;
	}
	public void setsRuttt(String sRuttt) {
		this.sRuttt = sRuttt;
	}
	public String getbMerito() {
		return bMerito;
	}
	public void setbMerito(String bMerito) {
		this.bMerito = bMerito;
	}
	public GetPDFSucursal(String sRutt, String sFolio, String sDoc, String sMonto, String sFecha, String sRuttt,
			String bMerito) {
		super();
		this.sRutt = sRutt;
		this.sFolio = sFolio;
		this.sDoc = sDoc;
		this.sMonto = sMonto;
		this.sFecha = sFecha;
		this.sRuttt = sRuttt;
		this.bMerito = bMerito;
	}
	
	
}
